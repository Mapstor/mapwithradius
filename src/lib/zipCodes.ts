// Look up a specific zip code
export function getZipCode(zip: string): ZipCodeData | null {
  return SAMPLE_ZIP_CODES.find(z => z.zip === zip) || null;
}

// Calculate haversine distance between two points
function haversineDistanceMiles(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const R = 3958.8; // Earth radius in miles
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLng / 2) * Math.sin(dLng / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
}

export interface ZipCodeWithDistance extends ZipCodeData {
  distance: number;
}

// Find all zip codes within a radius of a center point
export function findZipCodesWithinRadius(
  centerLat: number,
  centerLng: number,
  radiusMiles: number
): ZipCodeWithDistance[] {
  const results: ZipCodeWithDistance[] = [];

  for (const zipCode of SAMPLE_ZIP_CODES) {
    const distance = haversineDistanceMiles(centerLat, centerLng, zipCode.lat, zipCode.lng);
    if (distance <= radiusMiles) {
      results.push({
        ...zipCode,
        distance: Math.round(distance * 10) / 10 // Round to 1 decimal place
      });
    }
  }

  // Sort by distance
  results.sort((a, b) => a.distance - b.distance);

  return results;
}

// Convert zip codes to CSV format
export function zipCodesToCsv(zipCodes: ZipCodeWithDistance[]): string {
  const headers = ['Zip Code', 'City', 'State', 'Distance (miles)'];
  const rows = zipCodes.map(z => [z.zip, z.city, z.state, z.distance.toString()]);

  return [
    headers.join(','),
    ...rows.map(row => row.map(cell => `"${cell}"`).join(','))
  ].join('\n');
}
